// Helper function to get specific image by item title
export const getMenuItemImage = (itemTitle, fallbackImage = null) => {
  if (!itemTitle) {
    return fallbackImage;
  }
  
  // Try exact match first
  if (menuItemImageMapping[itemTitle]) {
    return menuItemImageMapping[itemTitle];
  }
  
  // Try case-insensitive match
  const exactMatch = Object.keys(menuItemImageMapping).find(
    key => key.toLowerCase() === itemTitle.toLowerCase()
  );
  if (exactMatch) {
    return menuItemImageMapping[exactMatch];
  }
  
  // Return fallback image if no match found
  return fallbackImage;
};

// Helper function to check if an item has a specific image
export const hasSpecificImage = (itemTitle) => {
  return menuItemImageMapping.hasOwnProperty(itemTitle) || 
         Object.keys(menuItemImageMapping).some(key => 
           key.toLowerCase() === itemTitle.toLowerCase()
         );
};